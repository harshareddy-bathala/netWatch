--
-- schema.sql - NetWatch Database Schema
-- =======================================
--
-- This file defines the database schema for NetWatch.
-- Production-ready schema with full table definitions, indexes, and constraints.
--

-- =============================================================================
-- DEVICES TABLE
-- =============================================================================
-- Stores information about devices seen on the network.
-- Each unique MAC address gets one record. Updated as traffic is captured.
-- Phase 1 migration 008: MAC-primary keying replaces IP-primary keying.

CREATE TABLE IF NOT EXISTS devices (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    mac_address TEXT NOT NULL UNIQUE,
    ip_address TEXT DEFAULT NULL,
    ipv4_address TEXT DEFAULT NULL,
    ipv6_address TEXT DEFAULT NULL,
    hostname TEXT DEFAULT NULL,
    device_name TEXT DEFAULT NULL,
    vendor TEXT DEFAULT NULL,
    first_seen TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    last_seen TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    total_bytes_sent INTEGER DEFAULT 0,
    total_bytes_received INTEGER DEFAULT 0,
    total_packets INTEGER DEFAULT 0,
    is_local INTEGER DEFAULT 0,
    device_type TEXT DEFAULT 'unknown',
    notes TEXT DEFAULT NULL,
    detected_mode TEXT DEFAULT NULL,
    active_mode TEXT DEFAULT NULL
);

-- =============================================================================
-- TRAFFIC_SUMMARY TABLE
-- =============================================================================
-- Stores individual packet/connection records for traffic analysis.
-- This is the main table for bandwidth and protocol analysis.

CREATE TABLE IF NOT EXISTS traffic_summary (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    timestamp TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    source_ip TEXT NOT NULL,
    dest_ip TEXT NOT NULL,
    source_mac TEXT DEFAULT NULL,
    dest_mac TEXT DEFAULT NULL,
    source_port INTEGER DEFAULT NULL,
    dest_port INTEGER DEFAULT NULL,
    protocol TEXT NOT NULL DEFAULT 'UNKNOWN',
    raw_protocol TEXT DEFAULT NULL,
    bytes_transferred INTEGER NOT NULL DEFAULT 0,
    packets_count INTEGER DEFAULT 1,
    direction TEXT DEFAULT 'unknown',
    session_id TEXT DEFAULT NULL,
    device_name TEXT DEFAULT NULL,
    vendor TEXT DEFAULT NULL
);

-- =============================================================================
-- ALERTS TABLE
-- =============================================================================
-- Stores alerts generated by the anomaly detection system.
-- Supports different severity levels and resolution tracking.

CREATE TABLE IF NOT EXISTS alerts (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    timestamp TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    alert_type TEXT NOT NULL CHECK(alert_type IN ('bandwidth', 'anomaly', 'device_count', 'health', 'protocol', 'connection', 'security', 'new_device', 'custom')),
    severity TEXT NOT NULL CHECK(severity IN ('info', 'low', 'medium', 'warning', 'high', 'critical')),
    message TEXT NOT NULL,
    details TEXT DEFAULT NULL,
    source_ip TEXT DEFAULT NULL,
    dest_ip TEXT DEFAULT NULL,
    resolved INTEGER DEFAULT 0,
    resolved_at TIMESTAMP DEFAULT NULL,
    resolved_by TEXT DEFAULT NULL,
    acknowledged INTEGER DEFAULT 0,
    acknowledged_at TIMESTAMP DEFAULT NULL
);

-- =============================================================================
-- BANDWIDTH_STATS TABLE
-- =============================================================================
-- Aggregated bandwidth statistics per minute for historical analysis.
-- Used for charts and trend analysis.

CREATE TABLE IF NOT EXISTS bandwidth_stats (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    timestamp TIMESTAMP NOT NULL,
    bytes_in INTEGER DEFAULT 0,
    bytes_out INTEGER DEFAULT 0,
    packets_in INTEGER DEFAULT 0,
    packets_out INTEGER DEFAULT 0,
    active_connections INTEGER DEFAULT 0,
    unique_devices INTEGER DEFAULT 0
);

-- =============================================================================
-- PROTOCOL_STATS TABLE
-- =============================================================================
-- Aggregated protocol statistics for analysis.

CREATE TABLE IF NOT EXISTS protocol_stats (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    timestamp TIMESTAMP NOT NULL,
    protocol TEXT NOT NULL,
    bytes_total INTEGER DEFAULT 0,
    packets_total INTEGER DEFAULT 0,
    connection_count INTEGER DEFAULT 0
);

-- =============================================================================
-- DAILY_USAGE TABLE
-- =============================================================================
-- Stores daily data usage per device for tracking over time.
-- Resets at midnight each day.

CREATE TABLE IF NOT EXISTS daily_usage (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    date TEXT NOT NULL,              -- YYYY-MM-DD format
    mac_address TEXT NOT NULL,
    ip_address TEXT,
    device_name TEXT,
    bytes_sent INTEGER DEFAULT 0,
    bytes_received INTEGER DEFAULT 0,
    total_bytes INTEGER DEFAULT 0,
    packet_count INTEGER DEFAULT 0,
    first_seen_today TIMESTAMP,
    last_seen_today TIMESTAMP,
    UNIQUE(date, mac_address)
);

CREATE INDEX IF NOT EXISTS idx_daily_usage_date ON daily_usage(date);
CREATE INDEX IF NOT EXISTS idx_daily_usage_mac ON daily_usage(mac_address);
CREATE INDEX IF NOT EXISTS idx_daily_usage_date_mac ON daily_usage(date, mac_address);

-- =============================================================================
-- SYSTEM_CONFIG TABLE
-- =============================================================================
-- Stores system configuration and metadata.

CREATE TABLE IF NOT EXISTS system_config (
    key TEXT PRIMARY KEY,
    value TEXT NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- =============================================================================
-- INDEXES FOR PERFORMANCE
-- =============================================================================
-- Phase 4 #45: traffic_summary has 8 composite/covering indexes.
-- Single-column indexes were removed — they are strict prefixes of the
-- composite ones and only add write amplification with no read benefit.
-- See migration 006_drop_redundant_indexes.sql for details.

-- Covering composite indexes for traffic_summary
CREATE INDEX IF NOT EXISTS idx_traffic_timestamp_protocol ON traffic_summary(timestamp, protocol);
CREATE INDEX IF NOT EXISTS idx_traffic_timestamp_direction ON traffic_summary(timestamp, direction);
CREATE INDEX IF NOT EXISTS idx_traffic_protocol_timestamp ON traffic_summary(protocol, timestamp);
CREATE INDEX IF NOT EXISTS idx_traffic_ts_bytes_dir ON traffic_summary(timestamp, bytes_transferred, direction);
CREATE INDEX IF NOT EXISTS idx_traffic_src_ip_ts_bytes ON traffic_summary(source_ip, timestamp, bytes_transferred);
CREATE INDEX IF NOT EXISTS idx_traffic_dst_ip_ts_bytes ON traffic_summary(dest_ip, timestamp, bytes_transferred);
CREATE INDEX IF NOT EXISTS idx_traffic_src_mac_ts_bytes ON traffic_summary(source_mac, timestamp, bytes_transferred);
CREATE INDEX IF NOT EXISTS idx_traffic_dst_mac_ts_bytes ON traffic_summary(dest_mac, timestamp, bytes_transferred);

-- Device indexes
CREATE INDEX IF NOT EXISTS idx_devices_mac ON devices(mac_address);
CREATE INDEX IF NOT EXISTS idx_devices_ip ON devices(ip_address);
CREATE INDEX IF NOT EXISTS idx_devices_ipv4 ON devices(ipv4_address);
CREATE INDEX IF NOT EXISTS idx_devices_last_seen ON devices(last_seen);
CREATE INDEX IF NOT EXISTS idx_devices_active_mode ON devices(active_mode);
-- Covering index for get_active_device_count(): scans last_seen range,
-- filters by active_mode/mac_address, and projects ip_address/ipv4_address
-- without touching the main table.
CREATE INDEX IF NOT EXISTS idx_devices_count_cover ON devices(last_seen, active_mode, mac_address, ip_address, ipv4_address);

-- Alert indexes
CREATE INDEX IF NOT EXISTS idx_alerts_timestamp ON alerts(timestamp);
CREATE INDEX IF NOT EXISTS idx_alerts_severity ON alerts(severity);
CREATE INDEX IF NOT EXISTS idx_alerts_type ON alerts(alert_type);
CREATE INDEX IF NOT EXISTS idx_alerts_resolved ON alerts(resolved);
CREATE INDEX IF NOT EXISTS idx_alerts_timestamp_severity ON alerts(timestamp, severity);

-- Bandwidth stats indexes
CREATE INDEX IF NOT EXISTS idx_bandwidth_timestamp ON bandwidth_stats(timestamp);

-- Protocol stats indexes
CREATE INDEX IF NOT EXISTS idx_protocol_stats_timestamp ON protocol_stats(timestamp);
CREATE INDEX IF NOT EXISTS idx_protocol_stats_protocol ON protocol_stats(protocol);

-- =============================================================================
-- TRAFFIC ROLLUP TABLE (hourly aggregation for long-term queries)
-- =============================================================================
-- Raw traffic_summary rows are aggregated here every hour, then raw rows
-- older than 24 h are deleted.  Keeps queries fast while retaining 90+ days
-- of hourly historical data.

CREATE TABLE IF NOT EXISTS traffic_rollup (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    hour_bucket TEXT NOT NULL,          -- 'YYYY-MM-DD HH:00:00'
    source_ip TEXT,
    dest_ip TEXT,
    protocol TEXT,
    direction TEXT DEFAULT 'unknown',
    total_bytes INTEGER DEFAULT 0,
    packet_count INTEGER DEFAULT 0,
    UNIQUE(hour_bucket, source_ip, dest_ip, protocol, direction)
);

CREATE INDEX IF NOT EXISTS idx_rollup_hour ON traffic_rollup(hour_bucket);
CREATE INDEX IF NOT EXISTS idx_rollup_protocol ON traffic_rollup(protocol);

-- =============================================================================
-- ALERT RULES TABLE (user-defined custom alert rules)
-- =============================================================================

CREATE TABLE IF NOT EXISTS alert_rules (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    description TEXT DEFAULT '',
    metric TEXT NOT NULL CHECK(metric IN ('bandwidth_bps', 'device_count', 'packet_rate', 'protocol_bytes')),
    operator TEXT NOT NULL CHECK(operator IN ('>', '<', '>=', '<=', '==')),
    threshold REAL NOT NULL,
    severity TEXT NOT NULL DEFAULT 'warning' CHECK(severity IN ('info', 'warning', 'critical')),
    enabled INTEGER NOT NULL DEFAULT 1,
    cooldown_seconds INTEGER NOT NULL DEFAULT 300,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    last_triggered_at TIMESTAMP DEFAULT NULL
);

-- =============================================================================
-- DEFAULT SYSTEM CONFIGURATION
-- =============================================================================

INSERT OR IGNORE INTO system_config (key, value) VALUES ('schema_version', '1.0.0');
INSERT OR IGNORE INTO system_config (key, value) VALUES ('created_at', datetime('now'));
INSERT OR IGNORE INTO system_config (key, value) VALUES ('last_cleanup', datetime('now'));
